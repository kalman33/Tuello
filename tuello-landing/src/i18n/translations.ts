export function t(lang: keyof typeof translations, key: keyof typeof translations['en']): string {
  return translations[lang][key] || translations['en'][key] || key;
}

export function getLanguageFromURL(url: URL): keyof typeof translations {
  const [, lang] = url.pathname.split('/');
  if (lang in translations) return lang as keyof typeof translations;
  return defaultLang;
}
